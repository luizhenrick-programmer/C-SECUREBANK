    private static String formatoCPF(String valor) {
        if (valor.length() == 11) {
            return String.format("%s.%s.%s-%s",
                    valor.substring(0, 3),
                    valor.substring(3, 6),
                    valor.substring(6, 9),
                    valor.substring(9));
        } else {
            return valor; // Retorna o valor original se não estiver completo
        }
    }

    private static String formatoRG(String valor) {
        if (valor.length() == 7) {
            return String.format("%s-%s",
                    valor.substring(0, 6),
                    valor.substring(6));
        } else {
            return valor; // Retorna o valor original se não estiver completo
        }
    }

    private static String formatoCEP(String valor) {
        if (valor.length() == 8) {
            return String.format("%s-%s",
                    valor.substring(0, 5),
                    valor.substring(5));
        } else {
            return valor; // Retorna o valor original se não estiver completo
        }
    }

    private static String formatoNascimento(String valor) {
        if (valor.length() == 8) {
            return String.format("%s/%s/%s",
                    valor.substring(0, 2),
                    valor.substring(2, 4),
                    valor.substring(4));
        } else {
            return valor; // Retorna o valor original se não estiver completo
        }
    }

    private static int GeradorNumeroConta() {
        Random random = new Random();
        int numeroAleatorio = random.nextInt(900000) + 100000;
        return numeroAleatorio;
    }
    
    
