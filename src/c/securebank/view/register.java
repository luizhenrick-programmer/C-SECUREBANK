    private void btFinalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btFinalizarMouseClicked
        String nome = txtNome.getText();
        String nascimento = txtNascimento.getText();
        String CPF = txtCPF.getText();
        String RG = txtRG.getText();
        Double saldo = 0.00;
        String usuario = txtUsuario.getText();
        String senha = txtPassword.getText();
        int numeroConta = GeradorNumeroConta();
        String rua = txtRua.getText();
        int numero = Integer.parseInt(txtNumero.getText());
        String setor = txtSetor.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        String cep = txtCEP.getText();
        String complemento = txtComplemento.getText();

        controllerCadastro cadastro = new controllerCadastro();
        controllerUsuario verificacao = new controllerUsuario();
        ConectaBD banco = new ConectaBD();
        

        try {    
            boolean ContaExiste = verificacao.verificarNumeroContaExistente(numeroConta);
            while (ContaExiste) {
                numeroConta = GeradorNumeroConta();            
            }
            SimpleDateFormat formatoEntrada = new SimpleDateFormat("dd/MM/yyyy");
            
            // Converte a string para um objeto Date usando o formato de entrada
            Date data = formatoEntrada.parse(nascimento);

            // Define o formato de saída
            SimpleDateFormat formatoSaida = new SimpleDateFormat("yyyy/MM/dd");

            // Converte o objeto Date para uma string usando o formato de saída
            String dataFormatada = formatoSaida.format(data);
            
            int EnderecoID = cadastro.inserirEndereco(rua, numero, setor, cidade, estado, cep, complemento);
            cadastro.inserirUsuario(nome, dataFormatada, CPF, RG, saldo, usuario, senha, numeroConta,EnderecoID);
            System.out.println("Cadastro realizado com sucesso!");
            this.dispose();
            login janela = new login();
            janela.setVisible(true);
            janela.setLocationRelativeTo(null);
            
        } catch (SQLException ex) {
            System.out.println("Erro ao inserir endereço: " + ex.getMessage());
        } catch (ParseException ex) {
            Logger.getLogger(register.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            banco.fecharConexao();
        }
    }//GEN-LAST:event_btFinalizarMouseClicked

