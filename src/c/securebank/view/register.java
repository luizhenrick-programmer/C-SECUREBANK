    private void txtNomeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyTyped
        char charKey = evt.getKeyChar();

        if (Character.isDigit(charKey)) {
            evt.consume();
        }

        String ValorAtual = txtNome.getText() + charKey;

        if (ValorAtual.length() < 10) {
            lbNome.setForeground(Color.red);
            lbNome.setText("Nome Inválido");
        } else {
            lbNome.setText("NOME");
            lbNome.setForeground(new Color(51, 102, 92));
        }
    }//GEN-LAST:event_txtNomeKeyTyped

